package br.com.ong.domain;

public class Ong {

	private int idOng;
	private int idLogin;
	private String nomeOng;
	private String descricaoOng;
	private int idTipoOng;
	private int idEndereco;
	private String dataFundacao;
	private int idStatusOng;
	private String telefoneOng;
	private String emailOng;

	public int getIdOng() {
		return idOng;
	}

	public void setIdOng(int id_Ong) {
		this.idOng = id_Ong;
	}

	public int getIdLogin() {
		return idLogin;
	}

	public void setIdLogin(int id_Login) {
		this.idLogin = id_Login;
	}

	public String getNomeOng() {
		return nomeOng;
	}

	public void setNomeOng(String nome_Ong) {
		this.nomeOng = nome_Ong;
	}

	public String getDescricaoOng() {
		return descricaoOng;
	}

	public void setDescricaoOng(String descricao_Ong) {
		this.descricaoOng = descricao_Ong;
	}

	public int getIdTipoOng() {
		return idTipoOng;
	}

	public void setIdTipoOng(int id_Tipo_Ong) {
		this.idTipoOng = id_Tipo_Ong;
	}

	public int getIdEndereco() {
		return idEndereco;
	}

	public void setIdEndereco(int id_Endereco) {
		this.idEndereco = id_Endereco;
	}

	public String getDataFundacao() {
		return dataFundacao;
	}

	public void setDataFundacao(String data_Fundacao) {
		this.dataFundacao = data_Fundacao;
	}

	public int getIdStatusOng() {
		return idStatusOng;
	}

	public void setIdStatusOng(int id_Status_Ong) {
		this.idStatusOng = id_Status_Ong;
	}

	public String getTelefoneOng() {
		return telefoneOng;
	}

	public void setTelefoneOng(String telefone_Ong) {
		this.telefoneOng = telefone_Ong;
	}

	public String getEmailOng() {
		return emailOng;
	}

	public void setEmailOng(String email_Ong) {
		this.emailOng = email_Ong;
	}

}
